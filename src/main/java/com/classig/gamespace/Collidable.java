package com.classig.gamespace;

/**
 * Интерфейс для объектов, которые могут сталкиваться друг с другом.
 * Поддерживает принцип разделения интерфейсов(Isp), предоставляя методы для проверки столкновений и получения координат/размера.
 */
public interface Collidable {

    /**
     * Проверяет, произошло ли столкновение с другим объектом.
     * @param other другой объект для проверки столкновения
     * @return true, если столкновение произошло, иначе false
     */
    boolean collide(Collidable other);

    /**
     * Возвращает X-координату центра объекта.
     * @return X-координата
     */
    int getPosX();

    /**
     * Возвращает Y-координату центра объекта.
     * @return Y-координата
     */
    int getPosY();

    /**
     * Возвращает размер объекта (диаметр для целей столкновения).
     * @return размер объекта
     */
    int getSize();
}
