package com.classig.gamespace;

/**
 * Интерфейс для объектов, которые могут быть отрисованы на игровом холсте.
 * Поддерживает принцип разделения интерфейсов (ISP), предоставляя только метод для отрисовки.
 */
public interface Drawable {

    /**
     * Отрисовывает объект на холсте с использованием предоставленного Renderer.
     * @param renderer объект для выполнения отрисовки
     */
    void draw(Renderer renderer);
}
